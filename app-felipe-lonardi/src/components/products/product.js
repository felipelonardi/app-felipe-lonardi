export const getProducts = (idCategory) => {
    return new Promise ((resolve) => {
        const productsToResolve = idCategory ? products.filter(item => item.category === idCategory) : products
        setTimeout(() => {
            resolve(productsToResolve);
        },500);
    });
}

export const getProduct = (id) => {
    return new Promise((resolve) => {
        const prod = products.find(p => p.id === parseInt(id))
        setTimeout(() => {
            resolve(prod)
        }, 500)
    })
}

export const getCategories = (categoryId) => {
    return new Promise((resolve) => {
        const filtro = products.filter(
            (product) =>product.category ===categoryId
        )
        setTimeout(() => {
            categoryId ? resolve(filtro) : resolve (products)
        }, 500)
    })
}


